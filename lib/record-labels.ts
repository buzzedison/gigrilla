/**
 * Get list of record label names for autocomplete
 */
export function getRecordLabelNames(): string[] {
  return RECORD_LABELS.map(label => label.name)
}

/**
 * Find a record label by name
 */
export function findRecordLabel(name: string): RecordLabel | undefined {
  const normalized = name.trim().toLowerCase()
  return RECORD_LABELS.find(label => label.name.toLowerCase() === normalized)
}
